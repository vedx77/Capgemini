//RETRIEVE FLIGHTS BASED ON SOURCE AND DESTINATION
public class JDBC2_Flight {
    private int flightId;
    private String source;
    private String destination;
    private int noOfSeats;
    private double flightFare;

    //GETTERS
    public int getFlightId() { return flightId; }
    public String getSource() { return source; }
    public String getDestination() { return destination; }
    public int getNoOfSeats() { return noOfSeats; }
    public double getFlightFare() { return flightFare; }

    //SETTERS
    public void setFlightId(int flightId) { this.flightId = flightId; }
    public void setFlightId(int flightId) { this.flightId = flightId; }
    public void setFlightId(int flightId) { this.flightId = flightId; }
    public void setFlightId(int flightId) { this.flightId = flightId; }
    public void setFlightFare(double flightFare) { this.flightFare = flightFare; }
}